package Ashlesha;

public class while_loop {

	public static void main(String[] args) {
		
		System.out.println("Using Loops: ");
	    int i = 1;
		
		while(i<=3) {
		 System.out.println(i);
		 i++;
		}
		System.out.println("Finish Running While loop!");
		
		while(true) {
			System.out.println("Infinite while loop!");
		}

	}

}

