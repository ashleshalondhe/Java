package Ashlesha;

public class do_while {

	public static void main(String[] args) {
		
		System.out.println("First loop: ");
		int a = 0;
		do {
			System.out.println(a);
			a++;
		} while(a<5);
		
		System.out.println("Second loop: ");
		
		int b = 1;
        do {
            System.out.println(b);
            b++;
        } while(b<=45);
		
		

	}

}
